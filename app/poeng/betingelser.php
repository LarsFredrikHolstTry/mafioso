        <h3>Betingelser for poengkjøp på Mafioso.no</h3>
        <h4>1. Avtalen</h4>
        Avtalen består av salgsbetingelsene, opplysninger gitt i bestillingsløsningen og eventuelt særskilt avtalte vilkår. 
        <br><br>
        <h4>2. Partene</h4>
        Selger er Holst-Try Webdesign (925 084 611), post@mafioso.no, og er dermed kontaktperson i denne avtalen.
        <br><br>
        <h4>3. Pris</h4>
        Prisene varierer for antall poeng som skal kjøpes fra selger (2.). Det er opp til selger (2.) hvilken type poeng-pakke som du ønsker å kjøpe.
        <br><br>
        <h4>4. Avtaleinngåelse</h4>
        Avtalen er bindende for begge parter når kjøperen har sendt sin bestilling til selgeren. Avtalen er likevel ikke bindende hvis det har forekommet skrive- eller tastefeil i tilbudet fra selgeren i bestillingsløsningen i nettbutikken eller i kjøperens bestilling, og den annen part innså eller burde ha innsett at det forelå en slik feil.
        <br><br>
        <h4>5. Betaling</h4>
        Selgeren krever betaling med en gang og poeng skal bli overført til bruker innen 24 timer. Det er ikke mulig å betale med faktura.
        <br><br>
        Kjøpere under 18 år kan ikke betale med andre enn sitt eget kredittkort eller debetkort.
        <br><br>
        <h4>6. Levering</h4>
        Poengene skal komme senest 24 timer inn på brukerkontoen, dersom det skulle skje en uventet handling fra nettsted skal pengene returneres til kjøper etter 14 dager.
        <br><br>
        <h4>7. Risiko for varen</h4>
        Risikoen for varen går over på kjøper når han, eller hans representant, har fått varen levert i tråd med punkt 6.
        <br><br>
        <h4>8. Angrerett</h4>
        Ved bruk av angreretten må varen leveres tilbake til selgeren uten unødig opphold og senest 14 dager fra melding om bruk av angreretten er gitt. Kjøper dekker de direkte kostnadene ved å returnere varen. Selgeren kan ikke fastsette gebyr for kjøperens bruk av angreretten.
